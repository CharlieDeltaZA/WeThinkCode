#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != c && i <= (ft_strlen(s) + 1))
		i++;
	if (!(s[i] == c))
		return (NULL);
	else
		return ((char*)&s[i]);
}
