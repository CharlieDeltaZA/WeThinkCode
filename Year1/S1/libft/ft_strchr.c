#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ret;
	int		i;

	i = 0;
	ret = NULL;
	while (s[i] != c)
		i++;
	if (s[i] == c)
		ret = &s[i];
	return ((char*)ret);
}
